#在编写函数时,可以给函数指定默认形参值,这样在调用函数时,如果没有传入该形参,则会使用默认值。

def greet(name, greeting="Hello"):
    print(greeting, name)

greet("Alice") # Output: Hello Alice
greet("Bob", "Hi") # Output: Hi Bob

#默认值可以是任何值,包括None,字符串,数字,列表,字典等。
#如果函数定义中有多个形参,则可以为其指定默认值,但必须将默认值置于最后一个形参之后。
#如果函数调用时没有传入所有形参,则会使用默认值。
#如果函数调用时传入了部分形参,则会使用传入的部分值,其余的形参使用默认值。
#如果函数调用时传入了所有形参,则会使用传入的全部值。
#如果函数调用时传入了不正确的类型,则会报错。
#如果函数调用时传入了不支持的关键字参数,则会报错。
#如果函数调用时传入了*args和**kwargs,则会报错。
#如果函数调用时传入了不支持的位置参数,则会报错。
